# Let's see the operators in action
### Intro to Data types & Operators
# - `+` `-` `*` `/`
### Comparision Operators
# - > greater than
# - < less than
# - == True or False
# - >= greater than or equal
# - <= less than or equal

a = 24
b = 16

print(a+b) # outcome added value of a & b
print(a-b) # outcome substraction value of a & b

# Comparison
print(a>b) # True
print(a<b) # False